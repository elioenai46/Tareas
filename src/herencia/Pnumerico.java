package herencia;

public class Pnumerico extends Primitivos{
	private byte bits;
	private short corto;
	private int entero;
	private long largo;
	
	public  Pnumerico() {
		
	
		
	}

	/**
	 * @return the bits
	 */
	public byte getBits() {
		return bits;
	}

	/**
	 * @param bits the bits to set
	 */
	public void setBits(byte bits) {
		this.bits = bits;
	}

	/**
	 * @return the corto
	 */
	public short getCorto() {
		return corto;
	}

	/**
	 * @param corto the corto to set
	 */
	public void setCorto(short corto) {
		this.corto = corto;
	}

	/**
	 * @return the entero
	 */
	public int getEntero() {
		return entero;
	}

	/**
	 * @param entero the entero to set
	 */
	public void setEntero(int entero) {
		this.entero = entero;
	}

	/**
	 * @return the largo
	 */
	public long getLargo() {
		return largo;
	}

	/**
	 * @param largo the largo to set
	 */
	public void setLargo(long largo) {
		this.largo = largo;
	}
	
	
}
